export function translate(locale: Locale, key: string, values: Record<string, unknown> = {}) {
  const template = messages[locale][key] ?? messages.zh[key] ?? key;
  return template.replace(/\{([^}]+)\}/g, (_, name: string) => String(values[name] ?? ""));
}

export function hasTranslation(locale: Locale, key: string) {
  return Boolean(messages[locale][key] || messages.zh[key]);
}
